def generate_dispatch_file(max_width: int = 8) -> str:
    """Generate toffoli_clifft_cdkm_dispatch.c with unified dispatch for all 4 variants."""
    output = []

    output.append("//")
    output.append(
        "// toffoli_clifft_cdkm_dispatch.c - Unified dispatch for all CDKM Clifford+T sequences"
    )
    output.append("//")
    output.append(
        "// Generated by scripts/generate_toffoli_clifft_cq_inc.py - DO NOT EDIT MANUALLY"
    )
    output.append("//")
    output.append("// Routes get_hardcoded_toffoli_clifft_*() calls to per-width implementations.")
    output.append(
        "// Each per-width file is conditionally compiled via #ifdef TOFFOLI_SEQ_WIDTH_N."
    )
    output.append("//")
    output.append("")
    output.append('#include "toffoli_sequences.h"')
    output.append("#include <stddef.h>")
    output.append("")

    # Forward declarations for all 4 variants
    variants = [
        ("QQ_add", "toffoli_clifft_QQ_add"),
        ("cQQ_add", "toffoli_clifft_cQQ_add"),
        ("CQ_inc", "toffoli_clifft_CQ_inc"),
        ("cCQ_inc", "toffoli_clifft_cCQ_inc"),
    ]

    output.append("// ============================================================================")
    output.append("// FORWARD DECLARATIONS (conditionally compiled per width)")
    output.append("// ============================================================================")
    output.append("")

    for _label, func_name in variants:
        for w in range(1, max_width + 1):
            output.append(f"#ifdef TOFFOLI_SEQ_WIDTH_{w}")
            output.append(f"const sequence_t *get_hardcoded_{func_name}_{w}(int bits);")
            output.append("#endif")
        output.append("")

    # Dispatch functions
    for label, func_name in variants:
        output.append(
            "// ============================================================================"
        )
        output.append(f"// UNIFIED TOFFOLI CLIFFT {label} DISPATCH")
        output.append(
            "// ============================================================================"
        )
        output.append("")
        output.append(f"const sequence_t *get_hardcoded_{func_name}(int bits) {{")
        output.append("    switch (bits) {")
        for w in range(1, max_width + 1):
            output.append(f"#ifdef TOFFOLI_SEQ_WIDTH_{w}")
            output.append(f"    case {w}: return get_hardcoded_{func_name}_{w}(bits);")
            output.append("#endif")
        output.append("    default: return NULL;")
        output.append("    }")
        output.append("}")
        output.append("")

    return "\n".join(output)


