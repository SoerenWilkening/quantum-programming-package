def validate(max_width: int = 8) -> bool:
    """Validate CQ/cCQ increment Clifford+T gate sequences."""
    all_ok = True

    # CQ inc layer counts
    print("=" * 60)
    print("Clifford+T CQ_inc layer count validation")
    print("=" * 60)
    for w in range(1, max_width + 1):
        cq_gates = generate_cq_inc_gates(w)
        clifft_gates = toffoli_to_clifford_t(cq_gates)
        actual = len(clifft_gates)
        # Count gate types in source
        x_count = sum(1 for g in cq_gates if g.num_controls == 0)
        cx_count = sum(1 for g in cq_gates if g.num_controls == 1)
        ccx_count = sum(1 for g in cq_gates if g.num_controls == 2)
        expected = x_count + cx_count + ccx_count * 15
        status = "OK" if actual == expected else "MISMATCH"
        if actual != expected:
            all_ok = False
        print(
            f"  width {w:2d}: {actual:4d} layers  "
            f"(src: X={x_count}, CX={cx_count}, CCX={ccx_count})  "
            f"expected {expected:4d}  [{status}]"
        )

    print()

    # cCQ inc layer counts
    print("=" * 60)
    print("Clifford+T cCQ_inc layer count validation")
    print("=" * 60)
    for w in range(1, max_width + 1):
        ccq_gates = generate_ccq_inc_gates(w)
        clifft_gates = toffoli_to_clifford_t(ccq_gates)
        actual = len(clifft_gates)
        x_count = sum(1 for g in ccq_gates if g.num_controls == 0)
        cx_count = sum(1 for g in ccq_gates if g.num_controls == 1)
        ccx_count = sum(1 for g in ccq_gates if g.num_controls == 2)
        expected = x_count + cx_count + ccx_count * 15
        status = "OK" if actual == expected else "MISMATCH"
        if actual != expected:
            all_ok = False
        print(
            f"  width {w:2d}: {actual:4d} layers  "
            f"(src: X={x_count}, CX={cx_count}, CCX={ccx_count})  "
            f"expected {expected:4d}  [{status}]"
        )

    print()

    # Verify max 1 control in Clifford+T gates
    print("=" * 60)
    print("Clifford+T CQ/cCQ inc gate control count (must be <= 1)")
    print("=" * 60)
    for w in range(1, max_width + 1):
        cq_gates = generate_cq_inc_gates(w)
        cq_clifft = toffoli_to_clifford_t(cq_gates)
        ccq_gates = generate_ccq_inc_gates(w)
        ccq_clifft = toffoli_to_clifford_t(ccq_gates)

        cq_max = max((1 if g.gate_type == "CX" else 0) for g in cq_clifft)
        ccq_max = max((1 if g.gate_type == "CX" else 0) for g in ccq_clifft)
        ok = cq_max <= 1 and ccq_max <= 1
        status = "OK" if ok else "FAIL"
        if not ok:
            all_ok = False
        print(f"  width {w:2d}: CQ max={cq_max}, cCQ max={ccq_max}  [{status}]")

    print()

    # Verify source gate counts match existing toffoli_cq_inc_seq_*.c
    print("=" * 60)
    print("CQ inc source gate count verification (vs existing files)")
    print("=" * 60)
    # Width 1: CQ = 1 gate (X), cCQ = 1 gate (CX)
    # Width 2: CQ = 12 gates (from toffoli_cq_inc_seq_2.c), cCQ = 12 gates
    # Width 3: CQ = 16 gates, cCQ = 16 gates
    # Width N >= 2: CQ = 4 + 4*(N-1) = 4N gates from gate analysis
    #   init: X(temp[0]), X(self[0]), X(carry) = 3 X
    #   folded MAJ bit 0: 1 CCX
    #   MAJ bits 1..N-1: (N-1) CCX
    #   UMA bits N-1..1: (N-1) * (CCX + CX + CX) = (N-1)*3 gates
    #   folded UMA bit 0: CCX + CX + CX = 3 gates
    #   cleanup: X(temp[0]) = 1 X
    #   Total: 3 + 1 + (N-1) + 3*(N-1) + 3 + 1 = 4 + N-1 + 3N-3 + 3 = 4N + 3
    # Wait, let me count for width 2:
    #   3 X + 1 CCX + 1 CCX (bits 1..1) + 1*(CCX+CX+CX) + (CCX+CX+CX) + 1 X
    #   = 4 + 2 + 3 + 3 = 12. Correct!
    # Width 3:
    #   3 X + 1 CCX + 2 CCX + 2*(CCX+CX+CX) + (CCX+CX+CX) + 1 X
    #   = 4 + 3 + 6 + 3 = 16. Correct!
    for w in range(1, max_width + 1):
        cq_gates = generate_cq_inc_gates(w)
        actual = len(cq_gates)
        if w == 1:
            expected = 1
        else:
            expected = 4 * w + 4
        status = "OK" if actual == expected else "MISMATCH"
        if actual != expected:
            all_ok = False
        print(f"  CQ  width {w:2d}: {actual:4d} gates  expected {expected:4d}  [{status}]")

    print()

    # Gate type distribution
    print("=" * 60)
    print("Clifford+T CQ/cCQ inc gate type distribution")
    print("=" * 60)
    for w in range(1, max_width + 1):
        cq_gates = generate_cq_inc_gates(w)
        cq_clifft = toffoli_to_clifford_t(cq_gates)
        counts = {"H": 0, "T": 0, "Tdg": 0, "X": 0, "CX": 0}
        for g in cq_clifft:
            counts[g.gate_type] += 1
        parts = ", ".join(f"{k}={v}" for k, v in counts.items() if v > 0)
        print(f"  CQ  width {w:2d}: {parts}, total={len(cq_clifft)}")

        ccq_gates = generate_ccq_inc_gates(w)
        ccq_clifft = toffoli_to_clifford_t(ccq_gates)
        counts = {"H": 0, "T": 0, "Tdg": 0, "X": 0, "CX": 0}
        for g in ccq_clifft:
            counts[g.gate_type] += 1
        parts = ", ".join(f"{k}={v}" for k, v in counts.items() if v > 0)
        print(f"  cCQ width {w:2d}: {parts}, total={len(ccq_clifft)}")

    print()

    if all_ok:
        print("ALL CQ/cCQ INC CLIFFORD+T VALIDATIONS PASSED")
    else:
        print("CQ/cCQ INC CLIFFORD+T VALIDATION FAILED - see mismatches above")

    return all_ok


