def main():
    parser = argparse.ArgumentParser(
        description=(
            "Generate CDKM CQ/cCQ increment Clifford+T sequence C files "
            "for widths 1-8, plus unified CDKM Clifford+T dispatch."
        )
    )
    parser.add_argument(
        "--width",
        type=int,
        metavar="N",
        help="Generate only for width N (default: all 1-8)",
    )
    parser.add_argument(
        "--dry-run",
        action="store_true",
        help="Print to stdout instead of writing files",
    )
    parser.add_argument(
        "--output-dir",
        type=str,
        default=None,
        help="Output directory for C files (default: c_backend/src/sequences/)",
    )
    parser.add_argument(
        "--validate",
        action="store_true",
        help="Cross-validate against expected layer counts",
    )
    parser.add_argument(
        "--max-width",
        type=int,
        default=8,
        help="Maximum width to generate (default: 8)",
    )

    args = parser.parse_args()

    if args.validate:
        ok = validate(args.max_width)
        sys.exit(0 if ok else 1)

    # Determine output directory
    script_dir = os.path.dirname(os.path.abspath(__file__))

    if args.output_dir:
        output_dir = args.output_dir
    else:
        output_dir = os.path.normpath(
            os.path.join(script_dir, "..", "c_backend", "src", "sequences")
        )

    max_width = args.max_width

    if args.width:
        if args.width < 1 or args.width > max_width:
            print(f"Error: width must be 1-{max_width}", file=sys.stderr)
            sys.exit(1)
        cq_content = generate_cq_inc_width_file(args.width)
        ccq_content = generate_ccq_inc_width_file(args.width)
        if args.dry_run:
            print(f"// === FILE: toffoli_clifft_cq_inc_{args.width}.c ===")
            print(cq_content)
            print()
            print(f"// === FILE: toffoli_clifft_ccq_inc_{args.width}.c ===")
            print(ccq_content)
        else:
            os.makedirs(output_dir, exist_ok=True)
            cq_path = os.path.join(output_dir, f"toffoli_clifft_cq_inc_{args.width}.c")
            with open(cq_path, "w") as f:
                f.write(cq_content)
            print(f"Written: {cq_path}")

            ccq_path = os.path.join(output_dir, f"toffoli_clifft_ccq_inc_{args.width}.c")
            with open(ccq_path, "w") as f:
                f.write(ccq_content)
            print(f"Written: {ccq_path}")
    else:
        # Full generation mode
        if args.dry_run:
            for w in range(1, max_width + 1):
                print(f"// === FILE: toffoli_clifft_cq_inc_{w}.c ===")
                print(generate_cq_inc_width_file(w))
                print()
                print(f"// === FILE: toffoli_clifft_ccq_inc_{w}.c ===")
                print(generate_ccq_inc_width_file(w))
                print()
            print("// === FILE: toffoli_clifft_cdkm_dispatch.c ===")
            print(generate_dispatch_file(max_width))
        else:
            os.makedirs(output_dir, exist_ok=True)

            # Write CQ inc files
            for w in range(1, max_width + 1):
                cq_content = generate_cq_inc_width_file(w)
                cq_path = os.path.join(output_dir, f"toffoli_clifft_cq_inc_{w}.c")
                with open(cq_path, "w") as f:
                    f.write(cq_content)
                print(f"Written: {cq_path}")

            # Write cCQ inc files
            for w in range(1, max_width + 1):
                ccq_content = generate_ccq_inc_width_file(w)
                ccq_path = os.path.join(output_dir, f"toffoli_clifft_ccq_inc_{w}.c")
                with open(ccq_path, "w") as f:
                    f.write(ccq_content)
                print(f"Written: {ccq_path}")

            # Write unified dispatch file
            dispatch_content = generate_dispatch_file(max_width)
            dispatch_path = os.path.join(output_dir, "toffoli_clifft_cdkm_dispatch.c")
            with open(dispatch_path, "w") as f:
                f.write(dispatch_content)
            print(f"Written: {dispatch_path}")

    return 0


if __name__ == "__main__":
    sys.exit(main() or 0)
