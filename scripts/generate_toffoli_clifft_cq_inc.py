#!/usr/bin/env python3
"""Toffoli CQ/cCQ increment Clifford+T generation script (widths 1-8).

This script generates per-width C source files for CQ and cCQ increment
(value=1) Clifford+T sequences, plus the unified CDKM Clifford+T dispatch file.

Generated files:
  - toffoli_clifft_cq_inc_N.c  (8 files) -- CQ increment Clifford+T
  - toffoli_clifft_ccq_inc_N.c (8 files) -- cCQ increment Clifford+T
  - toffoli_clifft_cdkm_dispatch.c       -- Unified dispatch for all 4 CDKM variants

The CQ/cCQ increment uses CDKM addition with value=1 classical-bit simplification:
  - Bit 0 skipped (carry=|0>), fold X-init at bit 1
  - Result: fewer gates than general QQ for the increment case

Gate encoding (Clifford+T):
  - H:   .Gate=H, .Target=t, .NumControls=0, .GateValue=0
  - T:   .Gate=T_GATE, .Target=t, .NumControls=0, .GateValue=M_PI/4.0
  - Tdg: .Gate=TDG_GATE, .Target=t, .NumControls=0, .GateValue=-M_PI/4.0
  - X:   .Gate=X, .Target=t, .NumControls=0, .GateValue=1
  - CX:  .Gate=X, .Target=t, .NumControls=1, .Control={c}, .GateValue=1

Usage:
  python scripts/generate_toffoli_clifft_cq_inc.py                  # Generate all files
  python scripts/generate_toffoli_clifft_cq_inc.py --width 4        # Generate only width 4
  python scripts/generate_toffoli_clifft_cq_inc.py --dry-run        # Print to stdout
  python scripts/generate_toffoli_clifft_cq_inc.py --validate       # Validate layer counts

Generated by: scripts/generate_toffoli_clifft_cq_inc.py
Phase 75, Plan 01: Clifford+T decomposed sequence generation for CDKM addition
"""

import argparse
import os
