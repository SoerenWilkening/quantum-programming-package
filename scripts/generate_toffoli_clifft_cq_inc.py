def generate_cq_inc_width_file(width: int) -> str:
    """Generate complete C source file for CQ inc Clifford+T at one width."""
    output = []

    output.append("//")
    output.append(
        f"// toffoli_clifft_cq_inc_{width}.c - CDKM CQ increment Clifford+T for {width}-bit width"
    )
    output.append("//")
    output.append(
        "// Generated by scripts/generate_toffoli_clifft_cq_inc.py - DO NOT EDIT MANUALLY"
    )
    output.append("//")
    output.append(
        "// Contains: Toffoli CDKM CQ increment (value=1) Clifford+T (static const, zero CCX)"
    )
    output.append(
        "// Algorithm: CDKM ripple-carry adder with classical-bit simplification + CCX->Clifford+T"
    )
    output.append("// All gates: H/T/Tdg/CX/X (max 1 control) -- fully static const.")
    output.append("//")
    output.append("")
    output.append('#include "toffoli_sequences.h"')
    output.append("")
    output.append(f"#ifdef TOFFOLI_SEQ_WIDTH_{width}")
    output.append("")
    if width >= 2:
        output.append("#include <math.h>")
        output.append("")

    # Generate gate sequences
    cq_gates = generate_cq_inc_gates(width)
    clifft_gates = toffoli_to_clifford_t(cq_gates)

    if width == 1:
        cq_num_qubits = 1
    else:
        cq_num_qubits = 2 * width + 1
    output.append("// ============================================================================")
    output.append(f"// TOFFOLI CLIFFT CQ_INC WIDTH {width} ({len(clifft_gates)} layers)")
    if width == 1:
        output.append("// Qubit layout: [0] = self")
    else:
        output.append(
            f"// Qubit layout: [0,{width - 1}] = temp, "
            f"[{width},{2 * width - 1}] = self, [{2 * width}] = carry"
        )
    output.append(f"// Total qubits: {cq_num_qubits}")
    output.append("// All gates: H/T/Tdg/CX/X (max 1 control) -- zero CCX")
    output.append("// ============================================================================")
    output.append("")
    output.append(generate_clifft_static_c(clifft_gates, "TOFFOLI_CLIFFT_CQ_INC", width))
    output.append("")

    # Dispatch helper
    output.append("// ============================================================================")
    output.append(f"// DISPATCH HELPER FOR WIDTH {width}")
    output.append("// ============================================================================")
    output.append("")
    output.append(f"const sequence_t *get_hardcoded_toffoli_clifft_CQ_inc_{width}(int bits) {{")
    output.append("    (void)bits;")
    output.append(f"    return &HARDCODED_TOFFOLI_CLIFFT_CQ_INC_{width};")
    output.append("}")
    output.append("")

    output.append(f"#endif // TOFFOLI_SEQ_WIDTH_{width}")
    output.append("")

    return "\n".join(output)


