def generate_cq_inc_gates(bits: int) -> list[ToffoliGate]:
    """Generate CQ increment (value=1) CDKM gate sequence.

    Qubit layout:
      [0..bits-1]       = temp register (classical init)
      [bits..2*bits-1]  = self register (quantum target)
      [2*bits]          = carry ancilla (bits >= 2)

    Width 1: single X(target=0) -- increment by 1 on 1-bit register
    Width N >= 2: CDKM with classical-bit simplification for value=1

    Classical-bit simplification for value=1 (binary = ...0001):
      - LSB (bit 0) = 1: skip MAJ at bit 0 (carry=|0>), fold X-init
      - All higher bits = 0: standard MAJ/UMA (no classical simplification)
    """
    gates = []

    if bits == 1:
        # Single X: self[0] ^= 1
        gates.append(ToffoliGate("X", target=0, controls=[]))
        return gates

    carry = 2 * bits

    # X-init temp[0] to classical bit 0 = 1
    gates.append(ToffoliGate("X", target=0, controls=[]))

    # Bit 0: classical bit = 1, carry-in = |0>
    # Skip MAJ at bit 0 (carry=|0>, so CCX with carry control never triggers)
    # Fold: CX(self[0], temp[0]) and CX(carry, temp[0]) simplified to:
    # X(self[0]) -- always flip self[0] since temp[0]=1 and carry=0
    # X(carry) -- carry gets the AND of temp[0] and self[0] original
    # Actually, the CDKM CQ for value=1 bit=0 uses:
    #   X-init temp[0]
    #   MAJ simplified: CX(self[0], temp[0]) is done but CCX(temp[0], carry, self[0]) has carry=|0>
    #   -> Simplified to: just set carry based on temp[0] AND self[0]
    # The actual pattern from toffoli_cq_inc_seq_2.c width 2:
    #   X(0), X(2), X(4), CCX(0,[4,2]), CCX(1,[0,3]), CCX(1,[0,3]), CX(0,[1]), CX(3,[0]),
    #   CCX(0,[4,2]), CX(4,[0]), CX(2,[4]), X(0)
    # Which is: X-init(temp[0]), X(self[0]), X(carry), CCX(temp[0],[carry,self[0]]),
    #   then standard MAJ(temp[0],self[1],temp[1]), UMA(temp[0],self[1],temp[1]),
    #   UMA(carry,self[0],temp[0]) with X-cleanup
    # Let me trace exactly from the existing files:

    # From toffoli_cq_inc_seq_2.c:
    # L0: X(0)         -- X-init temp[0] (classical bit 0 = 1)
    # L1: X(2)         -- X(self[0])
    # L2: X(4)         -- X(carry)
    # L3: CCX(0,[4,2]) -- CCX(temp[0], carry, self[0])
    # -- This is the "folded" MAJ at bit 0: effectively temp[0]=carry_out, carry is updated
    # L4: CCX(1,[0,3]) -- MAJ(temp[0], self[1], temp[1]): CX(self[1],temp[1]) part = CCX here?
    # Wait, this doesn't match MAJ pattern. Let me re-read more carefully.

    # Looking at toffoli_cq_inc_seq_3.c width 3 (16 layers):
    # L0:  X(0)          -- init temp[0] = 1
    # L1:  X(3)          -- X(self[0])
    # L2:  X(6)          -- X(carry)
    # L3:  CCX(0,[6,3])  -- CCX(temp[0],[carry,self[0]])  = "folded MAJ at bit 0"
    # L4:  CCX(1,[0,4])  -- at bit 1: MAJ(temp[0], self[1], temp[1])
    # L5:  CCX(2,[1,5])  -- at bit 2: MAJ(temp[1], self[2], temp[2])
    # L6:  CCX(2,[1,5])  -- UMA at bit 2 step 1: CCX(temp[2],[temp[1],self[2]])
    # L7:  CX(1,[2])     -- UMA at bit 2 step 2: CX(temp[1],[temp[2]])
    # L8:  CX(5,[1])     -- UMA at bit 2 step 3: CX(self[2],[temp[1]])
    # L9:  CCX(1,[0,4])  -- UMA at bit 1 step 1: CCX(temp[1],[temp[0],self[1]])
    # L10: CX(0,[1])     -- UMA at bit 1 step 2: CX(temp[0],[temp[1]])
    # L11: CX(4,[0])     -- UMA at bit 1 step 3: CX(self[1],[temp[0]])
    # L12: CCX(0,[6,3])  -- "folded UMA at bit 0": CCX(temp[0],[carry,self[0]])
    # L13: CX(6,[0])     -- UMA at bit 0 step 2: CX(carry,[temp[0]])
    # L14: CX(3,[6])     -- UMA at bit 0 step 3: CX(self[0],[carry])
    # L15: X(0)          -- X-cleanup temp[0]

    # Pattern for CQ inc value=1, bits >= 2:
    # 1. X-init temp[0] (classical bit 0 = 1)
    # 2. X(self[0]) and X(carry) -- fold the MAJ at bit 0
    # 3. CCX(temp[0], carry, self[0]) -- completes the "folded MAJ" at bit 0
    # 4. For bits 1..bits-1: standard MAJ (just the CCX part)
    #    MAJ(temp[i-1], self[i], temp[i]) = CCX(temp[i], temp[i-1], self[i])
    # 5. Reverse UMA sweep for bits bits-1..1:
    #    UMA(temp[i-1], self[i], temp[i]) = CCX(temp[i], temp[i-1], self[i]), CX(temp[i-1], temp[i]), CX(self[i], temp[i-1])
    # 6. "Folded UMA" at bit 0:
    #    CCX(temp[0], carry, self[0]), CX(carry, temp[0]), CX(self[0], carry)
    # 7. X-cleanup temp[0]

    # X(self[0]) and X(carry)
    gates.append(ToffoliGate("X", target=bits, controls=[]))  # X(self[0])
    gates.append(ToffoliGate("X", target=carry, controls=[]))  # X(carry)

    # Folded MAJ at bit 0: CCX(temp[0], carry, self[0])
    gates.append(ToffoliGate("X", target=0, controls=[carry, bits]))

    # Standard MAJ for bits 1..bits-1 (simplified: just CCX)
    for i in range(1, bits):
        # MAJ(temp[i-1], self[i], temp[i]) simplified to just CCX
        # CCX(temp[i], temp[i-1], self[i])
        gates.append(ToffoliGate("X", target=i, controls=[i - 1, bits + i]))

    # Reverse UMA sweep for bits bits-1..1
    for i in range(bits - 1, 0, -1):
        # UMA(temp[i-1], self[i], temp[i]):
        #   CCX(temp[i], temp[i-1], self[i])
        #   CX(temp[i-1], temp[i])
        #   CX(self[i], temp[i-1])
        gates.append(ToffoliGate("X", target=i, controls=[i - 1, bits + i]))
        gates.append(ToffoliGate("X", target=i - 1, controls=[i]))
        gates.append(ToffoliGate("X", target=bits + i, controls=[i - 1]))

    # Folded UMA at bit 0: CCX(temp[0], carry, self[0]), CX(carry, temp[0]), CX(self[0], carry)
    gates.append(ToffoliGate("X", target=0, controls=[carry, bits]))
    gates.append(ToffoliGate("X", target=carry, controls=[0]))
    gates.append(ToffoliGate("X", target=bits, controls=[carry]))

    # X-cleanup temp[0]
    gates.append(ToffoliGate("X", target=0, controls=[]))

    return gates


def generate_ccq_inc_gates(bits: int) -> list[ToffoliGate]:
    """Generate cCQ increment (value=1, controlled) CDKM gate sequence.

    Qubit layout:
      [0..bits-1]       = temp register
      [bits..2*bits-1]  = self register (quantum target)
      [2*bits]          = carry ancilla (bits >= 2)
      [2*bits+1]        = external control qubit

    Width 1: single CX(target=0, control=1)
    Width N >= 2: controlled CDKM increment with classical-bit simplification

    The controlled version replaces:
      X -> CX(target, ext_ctrl)
      CX -> CCX(target, ctrl, ext_ctrl)
      CCX -> MCX -> AND-decomposed -> 3 CCX via and_anc

    But since we're generating for Clifford+T, and cCQ inc files need to be
    static const (no MCX), we use the AND-ancilla decomposition pattern:
      - MCX(target, [c1, c2, ext_ctrl]) -> CCX(and_anc, c1, c2), CCX(target, and_anc, ext_ctrl), CCX(and_anc, c1, c2)
    Then all CCX -> 15 Clifford+T gates.

    For the Clifford+T version, we generate the controlled gate sequence
    using the decomposition pattern from the cCQ inc files.
    """
    gates = []

    if bits == 1:
        # Single CX: controlled self[0] ^= 1
        gates.append(ToffoliGate("X", target=0, controls=[1]))
        return gates

    carry = 2 * bits
    ext_ctrl = 2 * bits + 1

    # CX-init temp[0] (controlled by ext_ctrl)
    gates.append(ToffoliGate("X", target=0, controls=[ext_ctrl]))

    # Bit 0: controlled X(self[0]) and X(carry) become CCX
    gates.append(ToffoliGate("X", target=bits, controls=[0, ext_ctrl]))  # CCX
    gates.append(ToffoliGate("X", target=carry, controls=[0, ext_ctrl]))  # CCX

    # Folded cMAJ at bit 0: MCX(temp[0], [carry, self[0], ext_ctrl])
    # -> AND-decompose: CCX(and_anc, carry, self[0]), CCX(temp[0], and_anc, ext_ctrl),
    #                   CCX(and_anc, carry, self[0])
    # But we don't have and_anc in the qubit layout for cCQ inc!
    # Looking at existing cCQ inc files: they use MCX(3 controls) directly.
    # For Clifford+T we need AND-ancilla. Let me check qubit layout.
    #
    # From plan: "Qubit layout: [0..N-1] = temp, [N..2N-1] = self, [2N] = carry,
    #            [2N+1] = ext_ctrl, [2N+2] = AND-ancilla"
    # So we DO have and_anc at position 2*bits+2 for the Clifford+T version.
    and_anc = 2 * bits + 2

    # Folded cMAJ at bit 0: MCX(temp[0], [carry, self[0], ext_ctrl])
    # AND-decompose: CCX(and_anc, carry, self[0]), CCX(temp[0], and_anc, ext_ctrl), CCX(and_anc, carry, self[0])
    gates.append(ToffoliGate("X", target=and_anc, controls=[carry, bits]))
    gates.append(ToffoliGate("X", target=0, controls=[and_anc, ext_ctrl]))
    gates.append(ToffoliGate("X", target=and_anc, controls=[carry, bits]))

    # Standard cMAJ for bits 1..bits-1:
    # Each cMAJ(temp[i-1], self[i], temp[i], ext_ctrl) with AND-ancilla decomposition
    # Original gate: MCX(temp[i], [temp[i-1], self[i], ext_ctrl])
    # -> CCX(and_anc, temp[i-1], self[i]), CCX(temp[i], and_anc, ext_ctrl), CCX(and_anc, temp[i-1], self[i])
    for i in range(1, bits):
        gates.append(ToffoliGate("X", target=and_anc, controls=[i - 1, bits + i]))
        gates.append(ToffoliGate("X", target=i, controls=[and_anc, ext_ctrl]))
        gates.append(ToffoliGate("X", target=and_anc, controls=[i - 1, bits + i]))

    # Reverse cUMA sweep for bits bits-1..1
    # cUMA(temp[i-1], self[i], temp[i], ext_ctrl):
    #   MCX(temp[i], [temp[i-1], self[i], ext_ctrl]) -> 3 CCX via AND-ancilla
    #   CCX(temp[i-1], temp[i], ext_ctrl)
    #   CCX(self[i], temp[i-1], ext_ctrl)
    for i in range(bits - 1, 0, -1):
        # MCX decomposition
        gates.append(ToffoliGate("X", target=and_anc, controls=[i - 1, bits + i]))
        gates.append(ToffoliGate("X", target=i, controls=[and_anc, ext_ctrl]))
        gates.append(ToffoliGate("X", target=and_anc, controls=[i - 1, bits + i]))
        # CCX(temp[i-1], temp[i], ext_ctrl)
        gates.append(ToffoliGate("X", target=i - 1, controls=[i, ext_ctrl]))
        # CCX(self[i], temp[i-1], ext_ctrl)
        gates.append(ToffoliGate("X", target=bits + i, controls=[i - 1, ext_ctrl]))

    # Folded cUMA at bit 0: MCX(temp[0], [carry, self[0], ext_ctrl])
    # -> AND-decompose
    gates.append(ToffoliGate("X", target=and_anc, controls=[carry, bits]))
    gates.append(ToffoliGate("X", target=0, controls=[and_anc, ext_ctrl]))
    gates.append(ToffoliGate("X", target=and_anc, controls=[carry, bits]))
    # CCX(carry, temp[0], ext_ctrl)
    gates.append(ToffoliGate("X", target=carry, controls=[0, ext_ctrl]))
    # CCX(self[0], carry, ext_ctrl)
    gates.append(ToffoliGate("X", target=bits, controls=[carry, ext_ctrl]))

    # CX-cleanup temp[0]
    gates.append(ToffoliGate("X", target=0, controls=[ext_ctrl]))

    return gates


# ============================================================================
# C code generation for Clifford+T (static const)
# ============================================================================


def generate_clifft_static_c(gates: list[CliffordTGate], prefix: str, width: int) -> str:
    """Generate static const C code for a Clifford+T sequence."""
    lines = []
    num_layers = len(gates)

    # Each gate is on its own layer
    for i, gate in enumerate(gates):
        gate_str = gate.to_c_static()
        lines.append(f"static const gate_t {prefix}_{width}_L{i}[] = {{{gate_str}}};")
        lines.append("")

    # Layer pointer array
    if num_layers > 6:
        chunks = [list(range(i, min(i + 6, num_layers))) for i in range(0, num_layers, 6)]
        chunk_lines = []
        for chunk in chunks:
            names = ", ".join(f"{prefix}_{width}_L{j}" for j in chunk)
            chunk_lines.append(f"    {names}")
        layers_str = ",\n".join(chunk_lines)
        lines.append(f"static const gate_t *{prefix}_{width}_LAYERS[] = {{\n{layers_str}}};")
    else:
        names = ", ".join(f"{prefix}_{width}_L{i}" for i in range(num_layers))
        lines.append(f"static const gate_t *{prefix}_{width}_LAYERS[] = {{{names}}};")

    # Gates per layer array (all 1s)
    gpl_str = ", ".join("1" for _ in range(num_layers))
    lines.append(f"static const num_t {prefix}_{width}_GPL[] = {{{gpl_str}}};")
    lines.append("")

    # Sequence struct
    lines.append(
        f"static const sequence_t HARDCODED_{prefix}_{width} = "
        f"{{.seq = (gate_t **){prefix}_{width}_LAYERS,"
    )
    lines.append(f"                                              .num_layer = {num_layers},")
    lines.append(f"                                              .used_layer = {num_layers},")
    lines.append(
        f"                                              .gates_per_layer = "
        f"(num_t *){prefix}_{width}_GPL}};"
    )

    return "\n".join(lines)


